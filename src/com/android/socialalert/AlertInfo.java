package com.android.socialalert;

import java.io.Serializable;

public class AlertInfo implements Serializable {

    int NotificationId;

    public int getNotificationId() {
        return NotificationId;
    }

    public void setNotificationId(int notificationId) {
        NotificationId = notificationId;
    }

    int alertType;
    private String fromUserGuid;
    private String toUserGuid;
    private String Guid;
    private String latitude;
    private String longitude;
    boolean alertcheck;

    public int getAlertType() {
        return alertType;
    }

    public void setAlertType(int alertType) {
        this.alertType = alertType;
    }

    public String getFromUserGuid() {
        return fromUserGuid;
    }

    public void setFromUserGuid(String fromUserGuid) {
        this.fromUserGuid = fromUserGuid;
    }

    public String getToUserGuid() {
        return toUserGuid;
    }

    public void setToUserGuid(String toUserGuid) {
        this.toUserGuid = toUserGuid;
    }

    public String getGuid() {
        return Guid;
    }

    public void setGuid(String guid) {
        Guid = guid;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public boolean isAlertcheck() {
        return alertcheck;
    }

    public void setAlertcheck(boolean alertcheck) {
        this.alertcheck = alertcheck;
    }

}
