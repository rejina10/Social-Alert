package com.android.socialalert;

public class Person {

    private String firstName;
    private String lastName;
    private String address;
    private String dateTime;
    private String alertStatus;
    private String alertType;
    private String latitude;
    private String longitude;
    public int userStatus;

    public int getUserStatus() {
        return userStatus;
    }

    public void setUserStatus(int userStatus) {
        this.userStatus = userStatus;
    }

    public String getContactFirstName() {
        return firstName;
    }

    public void setContactFirstName(String contactFirstName) {
        this.firstName = contactFirstName;
    }

    public String getContactLastName() {
        return lastName;
    }

    public void setContactLastName(String contactLastName) {
        this.lastName = contactLastName;
    }

    public String getContactAddress() {
        return address;
    }

    public void setContactAddress(String contactAddress) {
        this.address = contactAddress;
    }

    public String getContactAlertType() {
        return alertType;
    }

    public void setContactAlertType(String alert) {
        this.alertType = alert;
    }

    public String getAlertStatus() {
        return alertStatus;
    }

    public void setAlertStatus(String status) {
        this.alertStatus = status;
    }

    public String getAlertSendDateTime() {
        return dateTime;
    }

    public void setAlertSendDateTimes(String alertSendDateTime) {
        this.dateTime = alertSendDateTime;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLongitude() {
        return longitude;
    }

}
